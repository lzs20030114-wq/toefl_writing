#!/usr/bin/env node
// Build data/REFERENCE_INVENTORY.md — the single source of truth for "what's
// really in the reference banks, by authenticity tier". Reads every reference
// file, counts by tier, separates ETS-official from everything else.
import { readFileSync, writeFileSync } from "fs";
import { resolve } from "path";
const root = resolve(import.meta.dirname, "..", "..");
const R = p => resolve(root, p);
const rd = p => { try { return JSON.parse(readFileSync(R(p), "utf8")); } catch { return null; } };
const arrOf = d => !d ? [] : (d.items || d.sets || d.samples || (Array.isArray(d) ? d : []));

const GROUPS = {
  "Build a Sentence": { files: ["data/buildSentence/tpo_official.json"], mdNote: true },
  "Academic Discussion": { files: ["data/academicWriting/real_tpo_reference.json", "data/academicWriting/recalled_supplement.json"] },
  "Write an Email": { files: ["data/emailWriting/tpo_reference.json", "data/emailWriting/practice_supplement.json"] },
  "Reading": { files: ["data/reading/samples/readInDailyLife/ets_official.json","data/reading/samples/readInDailyLife/third_party.json","data/reading/samples/readInDailyLife/goarno.json","data/reading/samples/readInDailyLife/ets_fulllength.json","data/reading/samples/academicPassage/ets_official.json","data/reading/samples/academicPassage/third_party.json","data/reading/samples/academicPassage/ets_fulllength.json","data/reading/samples/completeTheWords/ets_official.json","data/reading/samples/completeTheWords/third_party.json"] },
  "Listening": { files: ["data/listening/samples/lc-reference.json","data/listening/samples/la-reference.json","data/listening/samples/lat-reference.json","data/listening/samples/lcr-reference.json","data/listening/samples/lc-fulllength.json","data/listening/samples/la-fulllength.json","data/listening/samples/lat-fulllength.json"] },
  "Speaking": { files: ["data/speaking/samples/repeat-reference.json","data/speaking/samples/interview-reference.json","data/speaking/samples/repeat-fulllength.json","data/speaking/samples/interview-fulllength.json"] },
};
const TIERS = ["official", "recalled", "legacy", "third_party", "uncertain"];
const sum = o => Object.values(o).reduce((a, b) => a + b, 0);

const grand = {}; TIERS.forEach(t => grand[t] = 0);
const lines = [];
lines.push("# Reference Bank Inventory — by authenticity tier");
lines.push("");
lines.push("Auto-generated by `scripts/research/build-inventory.mjs` (re-run after any");
lines.push("reference change). Every item is tagged with a `tier` field. **No items were");
lines.push("deleted** — this is a labeling pass so banks can be filtered by trust level.");
lines.push("");
lines.push("## Tier meaning (most → least authentic)");
lines.push("- **official** — verbatim from an ETS-published PDF (Full-Length Practice Tests, Practice Sets). ETS-authored, but *practice material*, not leaked exam papers.");
lines.push("- **recalled** — reconstructed from an actually-administered 2026 exam (机经), dated & sourced. Real topic, third-party-reconstructed wording.");
lines.push("- **legacy** — in the repo before this audit, no recorded source; provenance unverifiable. Treat as practice-grade.");
lines.push("- **third_party** — collected from prep sites (goarno.io, toeflresources, magoosh, testsucceed…). Modeled on the task, NOT real exam items.");
lines.push("- **uncertain** — third-party, explicitly flagged as not traceable to any exam.");
lines.push("");
lines.push("## Counts by type and tier");
lines.push("");
lines.push("| Type | official | recalled | legacy | third_party | uncertain | TOTAL | non-official |");
lines.push("|------|--------:|--------:|------:|-----------:|---------:|------:|------------:|");

for (const [name, cfg] of Object.entries(GROUPS)) {
  const c = {}; TIERS.forEach(t => c[t] = 0);
  if (cfg.mdNote) {
    // Build a Sentence lives in tpo_source.md; official count from tpo_official.json
    c.official = arrOf(rd("data/buildSentence/tpo_official.json")).length;
    const md = readFileSync(R("data/buildSentence/tpo_source.md"), "utf8");
    const mdTot = (md.match(/^__\d+\./gm) || []).length;
    c.legacy = Math.max(0, mdTot - c.official); // remaining md items are recalled-transcribed (counted legacy here)
  } else {
    for (const f of cfg.files) for (const it of arrOf(rd(f))) { const t = (it && it.tier) || "legacy"; c[t] = (c[t] || 0) + 1; }
  }
  TIERS.forEach(t => grand[t] += (c[t] || 0));
  const tot = sum(c), nonOff = tot - (c.official || 0);
  lines.push(`| ${name} | ${c.official||0} | ${c.recalled||0} | ${c.legacy||0} | ${c.third_party||0} | ${c.uncertain||0} | ${tot} | ${nonOff} |`);
}
const gtot = sum(grand), gNonOff = gtot - grand.official;
lines.push(`| **TOTAL** | **${grand.official}** | **${grand.recalled}** | **${grand.legacy}** | **${grand.third_party}** | **${grand.uncertain}** | **${gtot}** | **${gNonOff}** |`);
lines.push("");
lines.push("## How to read this");
lines.push(`- **${grand.official}** items are ETS-official (practice PDFs).`);
lines.push(`- **${grand.recalled}** are recalled real-exam items (all Academic Discussion, examword 2026-03→05).`);
lines.push(`- **${grand.legacy + grand.third_party + grand.uncertain}** are legacy/third-party practice — modeled on the tasks but **not** real/official.`);
lines.push("- For Build a Sentence, the `legacy` count is the recalled-transcribed TPO items in `tpo_source.md` (real old-exam wording, hand-transcribed) — treat as better-than-third-party but not verbatim-official.");
lines.push("");
lines.push("## Note on Build a Sentence");
lines.push("BS items live in `data/buildSentence/tpo_source.md` (markdown, not JSON), so they");
lines.push("are not per-item tier-tagged. The 20 official items are mirrored in");
lines.push("`tpo_official.json` (tier=official); the other 60 are old-exam recall transcriptions.");
lines.push("");
lines.push("_To regenerate: `node scripts/research/build-inventory.mjs`_");

writeFileSync(R("data/REFERENCE_INVENTORY.md"), lines.join("\n") + "\n");
console.log("wrote data/REFERENCE_INVENTORY.md");
console.log("grand:", JSON.stringify(grand), "total", gtot, "non-official", gNonOff);
