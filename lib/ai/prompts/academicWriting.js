export function buildDiscussionUserPrompt(pd, text) {
  return [
    "题目类型: TOEFL Academic Discussion",
    `Professor: ${pd.professor.name}`,
    `Professor Post: ${pd.professor.text}`,
    ...pd.students.map((s, idx) => `Student ${idx + 1} (${s.name}): ${s.text}`),
    "",
    "考生回答:",
    text,
  ].join("\n");
}

